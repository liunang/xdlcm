package com.nantian.nfcm.app.vo;

public class AppProductBean {
    private String productNum;
    private String productName;
    private String qsNum;
    private String gbNum;
    private String firmNum;
    private String productType;
    private String productTech;
    private String shelfLife;
    private String quantity;
    private String ingredient;
    private String productionDate;

    public String getProductNum() {
        return productNum;
    }

    public void setProductNum(String productNum) {
        this.productNum = productNum;
    }

    public String getProductName() {
        return productName;
    }

    public void setProductName(String productName) {
        this.productName = productName;
    }

    public String getQsNum() {
        return qsNum;
    }

    public void setQsNum(String qsNum) {
        this.qsNum = qsNum;
    }

    public String getGbNum() {
        return gbNum;
    }

    public String getFirmNum() {
        return firmNum;
    }

    public void setFirmNum(String firmNum) {
        this.firmNum = firmNum;
    }

    public void setGbNum(String gbNum) {
        this.gbNum = gbNum;
    }

    public String getProductType() {
        return productType;
    }

    public void setProductType(String productType) {
        this.productType = productType;
    }

    public String getProductTech() {
        return productTech;
    }

    public void setProductTech(String productTech) {
        this.productTech = productTech;
    }

    public String getShelfLife() {
        return shelfLife;
    }

    public void setShelfLife(String shelfLife) {
        this.shelfLife = shelfLife;
    }

    public String getQuantity() {
        return quantity;
    }

    public void setQuantity(String quantity) {
        this.quantity = quantity;
    }

    public String getIngredient() {
        return ingredient;
    }

    public void setIngredient(String ingredient) {
        this.ingredient = ingredient;
    }

    public String getProductionDate() {
        return productionDate;
    }

    public void setProductionDate(String productionDate) {
        this.productionDate = productionDate;
    }
}
